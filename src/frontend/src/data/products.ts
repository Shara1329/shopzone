export interface Product {
  id: number;
  name: string;
  category: Exclude<Category, "all">;
  price: number;
  originalPrice: number;
  discount: number;
  rating: number;
  reviewCount: number;
  image: string;
  description: string;
  isNew: boolean;
  isFeatured: boolean;
  isDeal: boolean;
}

function p(
  id: number,
  name: string,
  category: Exclude<Category, "all">,
  price: number,
  originalPrice: number,
  rating: number,
  reviewCount: number,
  description: string,
  isNew: boolean,
  isFeatured: boolean,
  isDeal: boolean,
): Product {
  const discount = Math.round(((originalPrice - price) / originalPrice) * 100);
  return {
    id,
    name,
    category,
    price,
    originalPrice,
    discount,
    rating,
    reviewCount,
    image: categoryImages[category],
    description,
    isNew,
    isFeatured,
    isDeal,
  };
}

